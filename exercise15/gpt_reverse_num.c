#include <stdio.h>

int main() {
    int num, reversed = 0;

    printf("Введите положительное число: ");
    if (scanf("%d", &num) != 1) {
        printf("Ошибка при вводе числа.\n");
        return 1;
    }

    if (num < 0) {
        printf("Введено отрицательное число.\n");
        return 0;
    }

    while (num > 0) {
        int digit = num % 10;
        reversed = reversed * 10 + digit;
        num /= 10;
    }

    printf("Перевернутое число: %d\n", reversed);

    return 0;
}

