#include <stdio.h>
#include <string.h> // Для использования функции strlen

int main() {
    int pol = 1;
    char str[100];

    printf("Введите строку: ");
    if (scanf("%s", str) != 1) {
        printf("Ошибка при вводе строки.\n");
        return 1;
    }

    int len = strlen(str); // Получение длины строки

    int i = 0;
    while (i < len / 2) { // Проверка до половины длины
        if (str[i] != str[len - i - 1]) {
            pol = 0;
            break;
        }
        i++;
    }

    if (pol) {
        printf("Строка является палиндромом.\n");
    } else {
        printf("Строка не является палиндромом.\n");
    }

    return 0;
}

