//Отличный вопрос! Реализация проверок важна для того, чтобы ваша программа была
//более надежной и избегала возможных ошибок. Вот как вы можете добавить проверки
//и обработку ошибок в ваш код: 

 

#include <stdio.h>

int main() {
    int n1, n2;
    
    printf("Введите первое число: ");
    if (scanf("%d", &n1) != 1) {
        printf("Ошибка при вводе первого числа.\n");
        return 1;
    }

    printf("Введите второе число: ");
    if (scanf("%d", &n2) != 1) {
        printf("Ошибка при вводе второго числа.\n");
        return 1;
    }
    
    if (n2 == 0) {
        printf("Ошибка: деление на ноль недопустимо.\n");
        return 1;
    }

    printf("Сумма: %d\n", n1 + n2);
    printf("Разность: %d\n", n1 - n2);
    printf("Произведение: %d\n", n1 * n2);
    printf("Частное: %.2f\n", (float)n1 / n2);
    
    return 0;
}

/* В этом коде: 

	После каждого вызова  scanf, мы проверяем, успешно ли было считано одно
	целое число. Если не успешно (например, пользователь ввел не число), то
	выводим сообщение об ошибке и завершаем программу с кодом ошибки (ненулевым
	значением). 

	Перед делением на  n2, мы добавили проверку на то, что  n2 не равно нулю.
	Если равно, выводим сообщение об ошибке и завершаем программу. 

	Для вывода частного мы используем спецификатор формата  %.2f, который
	выводит число с двумя знаками после запятой. 

Это только пример, и вы можете внести свои изменения и улучшения в код. Главное,
помните о важности проверок и обработке ошибок для создания надежных программ. 
*/
