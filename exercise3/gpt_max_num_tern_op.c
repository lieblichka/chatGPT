#include <stdio.h>

int main() {
    int num1, num2, num3, max;
    
    printf("Введите первое число: ");
    if (scanf("%d", &num1) != 1) {
        printf("Ошибка при вводе первого числа.\n");
        return 1;
    }

    printf("Введите второе число: ");
    if (scanf("%d", &num2) != 1) {
        printf("Ошибка при вводе второго числа.\n");
        return 1;
    }

    printf("Введите третье число: ");
    if (scanf("%d", &num3) != 1) {
        printf("Ошибка при вводе третьего числа.\n");
        return 1;
    }
    
    max = (num1 > num2) ? num1 : num2;
    max = (max > num3) ? max : num3;
    
    printf("Максимальное число: %d\n", max);
    
    return 0;
}

//В этом варианте мы используем тернарные операторы для сравнения чисел и
//определения максимального значения. Каждый тернарный оператор сравнивает текущее
//максимальное число с очередным числом и, если очередное число больше,
//присваивает его в переменную  max. Таким образом, мы находим максимальное число
//с помощью последовательных сравнений.
