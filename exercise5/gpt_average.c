/*Ваш код в целом правильный и хорошо работает. Однако, существует небольшая
потенциальная проблема. Если пользователь введет отрицательное число сразу при
первом вводе, то ваша программа будет делать одну итерацию цикла и вычислять
среднее как 0, что не является правильным поведением.

Вот как можно немного улучшить ваш код, чтобы избежать этой 

*/

#include <stdio.h>

int main() {
    int num, total = 0, count = 0;

    printf("Введите число: ");
    while (scanf("%d", &num) == 1 && num >= 0) {
        total += num;
        count++;
        printf("Введите число: ");
    }

    if (count > 0) {
        printf("Среднее значение: %.2f\n", (float)total / count);
    } else {
        printf("Нет введенных положительных чисел.\n");
    }

    return 0;
}

/*В этой версии кода мы сначала вводим число до цикла и используем условие num >=
0 в начале цикла, чтобы избежать неправильного расчета среднего в случае, если
пользователь сразу ввел отрицательное число. Мы также добавили проверку count >
0, чтобы избежать деления на ноль при вычислении среднего, если не было введено
ни одного положительного числа.
*/
