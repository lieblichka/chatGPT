#include <stdio.h>

int main() {
    int num, is_prime = 1;

    printf("Введите число: ");
    if (scanf("%d", &num) != 1) {
        printf("Ошибка при вводе числа.\n");
        return 1;
    }

    if (num <= 1) {
        printf("%d - не является простым числом\n", num);
        return 0;
    }

    for (int i = 2; i * i <= num; i++) {
        if (num % i == 0) {
            is_prime = 0;
            break;
        }
    }

    if (is_prime) {
        printf("%d - простое число\n", num);
    } else {
        printf("%d - не является простым числом\n", num);
    }
    
    return 0;
}
/*
В этой версии я внес следующие изменения: 

	Заменил переменную  del на более осмысленное имя  is_prime, что делает код
	более читаемым. 

	Добавил проверку, что введенное число  num не меньше или равно 2 перед
	запуском цикла. Это позволяет избежать деления на ноль в случае ввода числа
	меньше 2. 

	Заменил цикл  for (int i = 2; i <= num - 1; i++)на for (int i = 2; i * i <=
	num; i++). Такой способ более эффективен, так как делает количество итераций
	меньше. Мы проверяем делители только до корня числа  num, так как если число
	делится на какое-то число больше корня, то оно обязательно делится и на
	какое-то число меньше корня.

В остальном, ваш код вполне хорош, и вы продемонстрировали хорошее понимание
логики проверки на простоту. 
*/
