//Чтобы немного упростить код, можно сделать так, чтобы переменная factorial сразу
//была инициализирована значением 1, даже в случае отрицательного числа:

 

#include <stdio.h>

int main() {
    int num, factorial = 1;

    printf("Введите число: ");
    if (scanf("%d", &num) != 1) {
        printf("Ошибка при вводе числа.\n");
        return 1;
    }

    if (num < 0) {
        printf("Невозможно вычислить факториал отрицательного числа.\n");
    } else {
        for (int i = 1; i <= num; i++) {
            factorial *= i;
        }
        printf("Факториал числа %d равен %d.\n", num, factorial);
    }

    return 0;
}

//В этой версии кода  factorial инициализируется значением 1 сразу в начале
//программы. Это позволяет избежать дублирования кода в блоке  else, так как
//factorial всегда будет иметь правильное значение, даже если число отрицательное. 
